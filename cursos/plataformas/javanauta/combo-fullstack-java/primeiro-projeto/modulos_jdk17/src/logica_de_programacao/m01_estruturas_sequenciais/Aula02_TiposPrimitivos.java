package logica_de_programacao.m01_estruturas_sequenciais;

public class Aula02_TiposPrimitivos {
    public static void main(String[] args) {
        int idade = 25;
        long quantidadeDeCachorros = 10L;
        System.out.println("Quantidade de cachorros = " + quantidadeDeCachorros);
        System.out.println("Idade = " + idade);
        double salarioMensal = 4500.00;
        System.out.println("Salário = " + salarioMensal);
        char genero = 'M';
        System.out.println("Gênero = " + genero);
        boolean empregado = true;
        System.out.println("Empregado = " + empregado);

    }
}
